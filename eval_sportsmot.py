import argparse
import os
import sys
from pathlib import Path

sys.path.insert(0, os.path.abspath(os.path.join(os.path.dirname(__file__), "external", "TrackEval")))
import trackeval  # noqa: E402


def resolve_dataset_root(data_root: str) -> Path:
    root = Path(data_root)
    candidates = [
        root,
        root / "dataset",
        root / "sportsmot_publish" / "dataset",
    ]
    for candidate in candidates:
        if (candidate / "val").is_dir():
            return candidate
    raise FileNotFoundError(f"Could not find SportsMOT dataset under {data_root}")


def main():
    parser = argparse.ArgumentParser("Evaluate SportsMOT validation results")
    parser.add_argument("--data_root", required=True, help="Extracted SportsMOT root or its parent directory")
    parser.add_argument("--trackers_folder", required=True, help="Folder containing tracker result directories")
    parser.add_argument("--tracker_name", required=True, help="Tracker directory name to evaluate")
    parser.add_argument("--split", default="val", choices=["train", "val"])
    parser.add_argument(
        "--seqmap_file",
        default="eval/TrackEval/data/gt/mot_challenge/seqmaps/sportsmot-val.txt",
        help="Seqmap file generated by prepare_sportsmot.py",
    )
    args = parser.parse_args()

    dataset_root = resolve_dataset_root(args.data_root)
    seqmap_file = args.seqmap_file
    if args.split != "val" and seqmap_file.endswith("sportsmot-val.txt"):
        seqmap_file = seqmap_file.replace("sportsmot-val.txt", f"sportsmot-{args.split}.txt")

    eval_config = trackeval.Evaluator.get_default_eval_config()
    eval_config["DISPLAY_LESS_PROGRESS"] = False
    eval_config["TIME_PROGRESS"] = True

    dataset_config = trackeval.datasets.MotChallenge2DBox.get_default_dataset_config()
    dataset_config.update(
        {
            "GT_FOLDER": str(dataset_root / args.split),
            "TRACKERS_FOLDER": args.trackers_folder,
            "TRACKERS_TO_EVAL": [args.tracker_name],
            "BENCHMARK": "SportsMOT",
            "SPLIT_TO_EVAL": args.split,
            "SEQMAP_FILE": seqmap_file,
            "SKIP_SPLIT_FOL": True,
            "DO_PREPROC": False,
            "TRACKER_SUB_FOLDER": "data",
            "OUTPUT_SUB_FOLDER": "",
        }
    )

    metrics_config = {"METRICS": ["HOTA", "CLEAR", "Identity"], "THRESHOLD": 0.5}
    evaluator = trackeval.Evaluator(eval_config)
    dataset_list = [trackeval.datasets.MotChallenge2DBox(dataset_config)]
    metrics_list = [
        trackeval.metrics.HOTA(metrics_config),
        trackeval.metrics.CLEAR(metrics_config),
        trackeval.metrics.Identity(metrics_config),
    ]
    evaluator.evaluate(dataset_list, metrics_list)


if __name__ == "__main__":
    main()
